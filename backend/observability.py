# =========================================================================
# Trace-id + bound-context plumbing
# =========================================================================
# Why a contextvar? Async request handlers cannot rely on thread-local
# storage â€” an ``asyncio`` task may hop between threads. A contextvar is
# propagated correctly through ``await`` boundaries and task spawning
# (``asyncio.create_task`` copies the current context), which is exactly the
# semantics the correlation middleware needs.
_trace_id_var: contextvars.ContextVar[Optional[str]] = contextvars.ContextVar(
    "ecm_trace_id", default=None
)
_bound_context_var: contextvars.ContextVar[Dict[str, Any]] = contextvars.ContextVar(
    "ecm_bound_context", default={}
)


def get_trace_id() -> Optional[str]:
    """Return the current request's trace id, or ``None`` outside a request."""
    return _trace_id_var.get()


def set_trace_id(trace_id: str) -> contextvars.Token:
    """Set the trace id for the current context. Returns a reset token."""
    return _trace_id_var.set(trace_id)


def reset_trace_id(token: contextvars.Token) -> None:
    """Reset the trace id using a token from :func:`set_trace_id`."""
    _trace_id_var.reset(token)


def bind_context(**fields: Any) -> contextvars.Token:
    """Attach key/value pairs to every log line in the current context.

    Typical use â€” a long-running restore job binds ``restore_id`` at the top,
    so every log line inside the task carries it without the call sites
    having to thread it through::

        token = bind_context(restore_id=restore.id, phase="validate")
        try:
            run_validation()
        finally:
            reset_context(token)

    The returned token can be passed to :func:`reset_context` to pop the
    binding (e.g. in a ``finally`` clause or a context manager).
    """
    current = dict(_bound_context_var.get())
    current.update(fields)
    return _bound_context_var.set(current)


def reset_context(token: contextvars.Token) -> None:
    """Undo a prior :func:`bind_context` call."""
    _bound_context_var.reset(token)


def generate_trace_id() -> str:
    """Return a fresh UUIDv4 string suitable for use as a trace id."""
    return str(uuid.uuid4())


# =========================================================================
# JSON logging
# =========================================================================
class _TraceIdFilter(logging.Filter):
    """Attach ``trace_id`` and any bound context to each log record.

    Runs as a filter (not a formatter) so the metadata is available to any
    formatter the operator plugs in downstream â€” including the JSON
    formatter we install below and any custom handlers that might want to
    consume structured attributes directly.
    """

    def filter(self, record: logging.LogRecord) -> bool:
        record.trace_id = _trace_id_var.get() or "-"
        bound = _bound_context_var.get()
        if bound:
            for key, value in bound.items():
                # Don't let bound context clobber reserved LogRecord attrs
                # (``message``, ``asctime``, etc.) â€” namespace-collide defensively.
                if not hasattr(record, key):
                    setattr(record, key, value)
        return True


# Fields we already represent explicitly in the JSON payload â€” skip them in
# the generic "extra" sweep so we don't double-emit.
_STANDARD_LOGRECORD_FIELDS = frozenset(
    {
        "name", "msg", "args", "levelname", "levelno", "pathname", "filename",
        "module", "exc_info", "exc_text", "stack_info", "lineno", "funcName",
        "created", "msecs", "relativeCreated", "thread", "threadName",
        "processName", "process", "message", "asctime", "taskName",
    }
)


class JsonFormatter(logging.Formatter):
    """Emit each log record as a single-line JSON document.

    Required fields â€” ``ts``, ``level``, ``logger``, ``msg``, ``trace_id`` â€”
    are always present. Any extra attributes attached to the LogRecord (via
    ``logger.info("...", extra={...})`` or :func:`bind_context`) flow into
    the top-level JSON object so downstream log pipelines can index on them
    without custom parsing.
    """

    def format(self, record: logging.LogRecord) -> str:  # noqa: A003 â€” stdlib signature
        payload: Dict[str, Any] = {
            "ts": self.formatTime(record, datefmt="%Y-%m-%dT%H:%M:%S") + f".{int(record.msecs):03d}Z",
            "level": record.levelname,
            "logger": record.name,
            "msg": record.getMessage(),
            "trace_id": getattr(record, "trace_id", "-"),
        }
        if record.exc_info:
            payload["exc_info"] = self.formatException(record.exc_info)
        if record.stack_info:
            payload["stack_info"] = self.formatStack(record.stack_info)

        # Sweep in any extra attributes (bound context, explicit extras).
        for key, value in record.__dict__.items():
            if key in _STANDARD_LOGRECORD_FIELDS or key == "trace_id":
                continue
            try:
                json.dumps(value)  # fast check for serializability
                payload[key] = value
            except (TypeError, ValueError):
                payload[key] = repr(value)

        return json.dumps(payload, default=str, separators=(",", ":"))


def install_json_logging(level: int = logging.INFO) -> None:
    """Swap the root logger's handlers for a JSON-emitting stream handler.

    Idempotent â€” calling twice leaves a single JSON handler in place. The
    ``_TraceIdFilter`` is applied on both the handler and the root logger
    so the trace id survives regardless of whether a subclassed logger adds
    its own handler.
    """
    root = logging.getLogger()
    json_handler = logging.StreamHandler()
    json_handler.setFormatter(JsonFormatter())
    json_handler.addFilter(_TraceIdFilter())

    # Drop any handler we previously installed (idempotency).
    for existing in list(root.handlers):
        if getattr(existing, "_ecm_json_handler", False):
            root.removeHandler(existing)
        else:
            # Also replace the formatter on any default stream handler so
            # ``logging.basicConfig``-installed handlers speak JSON too,
            # instead of emitting a second plaintext line alongside ours.
            if isinstance(existing, logging.StreamHandler):
                existing.setFormatter(JsonFormatter())
                existing.addFilter(_TraceIdFilter())

    json_handler._ecm_json_handler = True  # type: ignore[attr-defined]
    root.addHandler(json_handler)
    root.addFilter(_TraceIdFilter())
    root.setLevel(level)


